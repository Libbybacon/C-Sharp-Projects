using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CallingMethodsSubmissionAssignment
{
    public class Math
    {
        //Create three methods, each one takes one integer parameter in and returns an integer
        public static int AddTwo(int num)
        {
            return num + 2;

        }

        public static int DivideByTwo(int num)
        {
            return num / 2;
        }

        public static int MultiplyByTwo(int num)
        {
            return num * 2;
        }
    }
